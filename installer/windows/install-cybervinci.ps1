        CreateStartMenuShortcut = $startMenu.Checked
    }
}

if (!(Test-Path -LiteralPath $profilePath)) {
    throw "CyberVinci install profile not found: $profilePath"
}

$profile = Read-JsonFile $profilePath

if (!$NoWizard) {
    $wizard = Show-InstallWizard $profile $InstallDir
    if ($wizard) {
        $InstallDir = $wizard.InstallDir
        $CreateDesktopShortcut = [switch]$wizard.CreateDesktopShortcut
        $CreateStartMenuShortcut = [switch]$wizard.CreateStartMenuShortcut
    }
}

$installFullPath = [System.IO.Path]::GetFullPath($InstallDir)
$payloadFullPath = [System.IO.Path]::GetFullPath($payloadRoot)
if ($installFullPath.StartsWith($payloadFullPath, [System.StringComparison]::OrdinalIgnoreCase)) {
    throw "InstallDir cannot be inside the payload directory."
}

New-Item -ItemType Directory -Force -Path $installFullPath | Out-Null
robocopy $payloadRoot $installFullPath /E /XD .git .codex-run .playwright-mcp /XF *.log *.tmp /NFL /NDL /NJH /NJS /NP | Out-Null
if ($LASTEXITCODE -gt 7) {
    throw "robocopy failed for $payloadRoot -> $installFullPath ($LASTEXITCODE)"
}

$installedProfile = Join-Path $installFullPath $profileRelativePath
Write-JsonFile $installedProfile $profile

$launcher = Join-Path $installFullPath "CyberVinci.vbs"
if (!(Test-Path -LiteralPath $launcher)) {
    throw "Installed launcher not found: $launcher"
}
$wscript = Join-Path $env:WINDIR "System32\wscript.exe"
$icon = Join-Path $installFullPath "resources\cybervinci.ico"
if (!(Test-Path -LiteralPath $icon)) {
    $icon = Join-Path $installFullPath "node_modules\electron\dist\electron.exe"
}

if ($CreateDesktopShortcut) {
    $desktopDir = [Environment]::GetFolderPath('Desktop')
    New-Shortcut (Join-Path $desktopDir "CyberVinci.lnk") $wscript $installFullPath "`"$launcher`"" $icon
}

if ($CreateStartMenuShortcut) {
    $programsDir = [Environment]::GetFolderPath('Programs')
    $startMenuDir = Join-Path $programsDir "CyberVinci"
    New-Item -ItemType Directory -Force -Path $startMenuDir | Out-Null
    New-Shortcut (Join-Path $startMenuDir "CyberVinci.lnk") $wscript $installFullPath "`"$launcher`"" $icon
}

Write-Host "CyberVinci installed at $installFullPath" -ForegroundColor Green
Write-Host "Launcher: $launcher"
