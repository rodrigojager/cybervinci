    $core.Text = "CyberVinci Desktop core"
    $core.Left = 18
    $core.Top = 28
    $core.Width = 540
    $core.Checked = $true
    $core.Enabled = $false
    $components.Controls.Add($core)

    $csharp = New-Object System.Windows.Forms.CheckBox
    $csharp.Text = "C# and .NET tooling"
    $csharp.Left = 18
    $csharp.Top = 54
    $csharp.Width = 540
    $csharp.Checked = Get-ComponentState $Profile "csharp-dotnet"
    $csharp.Enabled = $false
    $components.Controls.Add($csharp)

    $codex = New-Object System.Windows.Forms.CheckBox
    $codex.Text = "Codex extension"
    $codex.Left = 18
    $codex.Top = 80
    $codex.Width = 540
    $codex.Checked = Get-ComponentState $Profile "codex-extension"
