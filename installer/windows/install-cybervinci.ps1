[CmdletBinding()]
param(
    [string]$InstallDir = "$env:LOCALAPPDATA\Programs\CyberVinci",
    [switch]$NoWizard,
    [switch]$CreateDesktopShortcut,
    [switch]$CreateStartMenuShortcut
)

$ErrorActionPreference = 'Stop'

$payloadRoot = Split-Path -Parent $MyInvocation.MyCommand.Path
$profileRelativePath = "examples/electron/resources/cybervinci-install-profile.json"
$profilePath = Join-Path $payloadRoot $profileRelativePath

function Read-JsonFile([string]$Path) {
    return Get-Content -LiteralPath $Path -Raw | ConvertFrom-Json
}

function Write-JsonFile([string]$Path, [object]$Value) {
    $parent = Split-Path -Parent $Path
    if (!(Test-Path -LiteralPath $parent)) {
        New-Item -ItemType Directory -Force -Path $parent | Out-Null
    }
    $Value | ConvertTo-Json -Depth 100 | Set-Content -LiteralPath $Path -Encoding UTF8
}

function Get-ComponentState([object]$Profile, [string]$Id) {
    foreach ($component in $Profile.components) {
        if ($component.id -eq $Id) {
            return [bool]$component.selected
        }
    }
    return $false
}

function New-Shortcut([string]$ShortcutPath, [string]$TargetPath, [string]$WorkingDirectory, [string]$Arguments, [string]$IconPath) {
    $shell = New-Object -ComObject WScript.Shell
    $shortcut = $shell.CreateShortcut($ShortcutPath)
    $shortcut.TargetPath = $TargetPath
    $shortcut.Arguments = $Arguments
    $shortcut.WorkingDirectory = $WorkingDirectory
    $shortcut.IconLocation = $IconPath
    $shortcut.Save()
}

