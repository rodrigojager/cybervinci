    $title.Text = "Install CyberVinci"
    $title.Font = New-Object System.Drawing.Font("Segoe UI", 16, [System.Drawing.FontStyle]::Bold)
    $title.Left = 24
    $title.Top = 20
    $title.Width = 620
    $title.Height = 32
    $form.Controls.Add($title)

