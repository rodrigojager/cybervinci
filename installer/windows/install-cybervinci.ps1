    $desc.Text = "This wizard installs the CyberVinci payload generated by the release build. Optional Theia extensions are selected when the payload is built, then recorded here in the installed profile."
    $desc.Left = 24
    $desc.Top = 62
    $desc.Width = 620
    $desc.Height = 48
    $form.Controls.Add($desc)

    $installLabel = New-Object System.Windows.Forms.Label
    $installLabel.Text = "Install location"
    $installLabel.Left = 24
    $installLabel.Top = 124
    $installLabel.Width = 150
    $installLabel.Height = 22
    $form.Controls.Add($installLabel)

    $installBox = New-Object System.Windows.Forms.TextBox
    $installBox.Left = 180
    $installBox.Top = 122
    $installBox.Width = 430
    $installBox.Text = $DefaultInstallDir
